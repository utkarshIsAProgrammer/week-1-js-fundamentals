// String Conversion

let num = 99;
console.log(num, typeof num);

num = String(num);
console.log(num, typeof num);

// Number Conversion

let strNum = "99";
console.log(num, typeof num);

strNum = Number(strNum);
console.log(strNum, typeof strNum);

// Boolean Conversion
let zero = "";
console.log(zero, typeof zero);

zero = Boolean(zero);
console.log(zero, typeof zero);

let empty = " ";
empty = Boolean(empty);
console.log(empty, typeof empty);
