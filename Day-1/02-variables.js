// Declaration
let message;

// Assignment
message = "Hello!";
console.log(message);

// Declaration + Assignment
let newMessage = "Hello World from JS!";
console.log(newMessage);

// Re-declaration
newMessage = "Hello World from Web!";
console.log(newMessage);

// Multiple variable in one line
let user = "Panchajanya",
	age = 19,
	isLearning = true;
console.log(user);

// Copy data from one variable to another
let hello = "Hello";
let helloMessage = hello;
console.log(hello);
console.log(helloMessage);

// Constant
const myBirthday = "10 December 2005";
console.log(myBirthday);

const BLACK_COLOR = "#000";
console.log(BLACK_COLOR);
